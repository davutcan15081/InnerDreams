import 'package:shared_preferences/shared_preferences.dart';

enum AppLocale {
  turkish,
  english,
}

class LocaleNotifier extends StateNotifier<AppLocale> {
  LocaleNotifier() : super(AppLocale.turkish) {
    _loadLocale();
  }

  Future<void> _loadLocale() async {
    try {
      final prefs = await SharedPreferences.getInstance();
      final localeIndex = prefs.getInt('app_locale');
      if (localeIndex != null) {
        state = AppLocale.values[localeIndex];
      }
    } catch (e) {
      print('Locale load error: $e');
      state = AppLocale.turkish;
    }
  }

  Future<void> setLocale(AppLocale locale) async {
    try {
      state = locale;
      final prefs = await SharedPreferences.getInstance();
      await prefs.setInt('app_locale', locale.index);
    } catch (e) {
      print('Locale save error: $e');
    }
  }

  Locale get locale {
    switch (state) {
      case AppLocale.turkish:
        return const Locale('tr', 'TR');
      case AppLocale.english:
        return const Locale('en', 'US');
    }
  }

  bool get isTurkish => state == AppLocale.turkish;
  bool get isEnglish => state == AppLocale.english;
}

final localeProvider = StateNotifierProvider<LocaleNotifier, AppLocale>((ref) {
  return LocaleNotifier();
});

final localeDataProvider = Provider<Locale>((ref) {
  return ref.watch(localeProvider.notifier).locale;
});

